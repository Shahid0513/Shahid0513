import java.lang.Math;
public class MathClassDemo {

	public static void main(String[] args) {

		System.out.println("power of given number -> "+(int) Math.pow(2, 2));
		
		System.out.println("square root of given number -> "+(int)Math.sqrt(49));
		
		System.out.println("absolute value -> "+Math.abs(-49.7));
		
		System.out.println("minimum between 2 number -> "+Math.min(2, 5));
		
		System.out.println("maximum between 2 -> "+Math.max(2, 5));
		
		System.out.println("random number -> "+(int)Math.random());
		
		
	}

}
